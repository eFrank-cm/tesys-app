<p class="text-balance">
    Estas son las acciones que debe realizar el <strong>docente asesor</strong> al
    recibir la solicitud de asesoramiento de tesis:
</p>

<ol class="ml-10 list-decimal text-balance">
    <li>
        <strong>Revisar</strong> la solicitud y la propuesta de tesis (título, descripción,
        objetivos y relevancia) para verificar su claridad y viabilidad.
    </li>
    <li>
        <strong>Decidir</strong> sobre la invitación:
        <ul class="ml-6 list-disc mt-2">
            <li>
                <strong>Aceptar:</strong> confirma su disposición para asesorar el
                proyecto.
            </li>
            <li>
                <strong>Denegar:</strong> declina la invitación; no se generan acciones
                adicionales y no formará parte del proyecto.
            </li>
        </ul>
    </li>
    <li>
        <strong>Si acepta</strong>:
        <ul class="ml-6 list-disc mt-2">
            <li>
                Firmar la <strong>carta de aceptación</strong> (según el formato
                indicado).
            </li>
            <li class="opacity-80">
                Opcional: coordinar una reunión inicial con el tesista para
                alinear expectativas (alcance, cronograma y canales de
                comunicación).
            </li>
        </ul>
    </li>
</ol>

<p class="text-balance">
    Nota: La firma de la carta de aceptación formaliza su rol como asesor y
    habilita el seguimiento académico del proyecto.
</p>
